CREATE TABLE XXBCM_SUPPLIER
(
  SUPPLIER_NAME             VARCHAR2(50 BYTE)   NOT NULL,
  SUPP_FIRST_NAME           VARCHAR2(50 BYTE),
  SUPP_LAST_NAME            VARCHAR2(50 BYTE),
  SUPP_CONTACT_NAME         VARCHAR2(100 BYTE),
  SUPP_CONTACT_NUMBER1  NUMBER,
  SUPP_CONTACT_NUMBER2  NUMBER,
  SUPP_EMAIL                VARCHAR2(100 BYTE),
  CONSTRAINT supplier_pk PRIMARY KEY (supplier_name)
);


CREATE TABLE XXBCM_ORDER_INV
(
  INVOICE_REFERENCE    VARCHAR2(50 BYTE),
  ORIG_ORDER_REF       VARCHAR2(10 BYTE),
  INVOICE_DATE         DATE,
  INVOICE_STATUS       VARCHAR2(20 BYTE),
  INVOICE_HOLD_REASON  VARCHAR2(200 BYTE),
  ORDER_REF            VARCHAR2(10 BYTE),
  INVOICE_AMOUNT       NUMBER,
  INVOICE_DESCRIPTION  VARCHAR2(200 BYTE),
   CONSTRAINT order_inv_pk PRIMARY KEY (INVOICE_REFERENCE)
);


CREATE TABLE XXBCM_ORDER_HDR
(
  ORDER_REF           VARCHAR2(10 BYTE)         NOT NULL,
  ORDER_DATE          DATE,
  SUPPLIER_NAME       VARCHAR2(50 BYTE),
  ORDER_TOTAL_AMOUNT  NUMBER,
  CONSTRAINT ORDER_REF_pk PRIMARY KEY (ORDER_REF)
);

CREATE TABLE XXBCM_ORDER_DET
(
  ORDER_REF          VARCHAR2(10 BYTE)          NOT NULL,
  ORDER_REF_SEQ      NUMBER,
  ORIG_ORDER_REF     VARCHAR2(10 BYTE),
  ORDER_DESCRIPTION  VARCHAR2(100 BYTE),
  ORDER_STATUS       VARCHAR2(20 BYTE),
  ORDER_LINE_AMOUNT  NUMBER,
  INVOICE_REFERENCE  VARCHAR2(30 BYTE),
  CONSTRAINT ORDER_DET_pk PRIMARY KEY (ORDER_REF, order_ref_seq)
);